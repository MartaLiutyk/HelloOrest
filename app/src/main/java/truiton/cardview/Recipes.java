public class Recipes {
    private String name;
    private int idPhoto;


    public Recipes(String name, int idPhoto) {
        this.name = name;
        this.idPhoto = idPhoto;
    }

    public String getName() {
        return name;
    }

    public int getIdPhoto() {
        return idPhoto;
    }

}

